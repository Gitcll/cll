package com.chinasoft.entity;
/**
 * 
 * @author Administrator
 *药品表
 */
public class Medicine {
	private int mid; //药品序号
	private String mname;//药品名称
	private int num;//药品数量
	private String unit;//单位
	private String descp;//药品说明
	private double price;//药品价格
	public Medicine(int mid, String mname, int num, String unit, String descp,
			double price) {
		super();
		this.mid = mid;
		this.mname = mname;
		this.num = num;
		this.unit = unit;
		this.descp = descp;
		this.price = price;
	}
	public Medicine() {
		super();
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getNuit() {
		return unit;
	}
	public void setNuit(String unit) {
		this.unit = unit;
	}
	public String getDescp() {
		return descp;
	}
	public void setDescp(String descp) {
		this.descp = descp;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	@Override
	public String toString() {
		return "Medicine [mid=" + mid + ", mname=" + mname + ", num=" + num
				+ ", unit=" + unit + ", descp=" + descp + ", price=" + price
				+ "]";
	}
	
	
	
}
